public class ShiftOperators {  
    public static void main( String[ ] args ) {  
        int x = 5 ;  
        int y = x << 4 ;  
        int v = 60; 
        int z = v >> 4;
        System.out.println( " The value is: " +y) ;  
        System.out.println( " The value is: " +z) ;  
    }  
}  